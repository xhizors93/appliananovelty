                                    	<li>Program ini juga bertujuan untuk memastikan murid daripada keluarga yang kurang mampu tidak tercicir dalam persekolahan mereka.</li>
                                    	<li>Membuktikan keperihatinan pihak Kerajaan dalam usaha memartabatkan ilmu pengetahuan dalam kalangan masyarakat</li>
                                    	<li>Dapat mewujudkan daya saing dalam kalangan masyarakat luar Bandar dengan masyarakat Bandar.</li>
                                    	<li>Memudahkan proses pengajaran dan pembelajaran.</li>
                                    </ul>
