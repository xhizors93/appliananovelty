							<h3>SPBT Background</h3>
                                <p>
                                    Program Skim Pinjaman Buku Teks (SPBT) mula dilaksanakan pada tahun 1975. SPBT adalah merupakan satu program memberi pinjaman buku teks kepada murid yang layak di semua Sekolah Kerajaan dan Sekolah Bantuan Kerajaan.Program ini terhad kepada yang ibu bapa mereka warganegara Malaysia sahaja. Pada tahun 1983, program ini diperluaskan ke Sekolah Menengah Agama Rakyat (SMAR). Bermula pada tahun 2008 semua pelajar tidak mengira status kewangan layak menerima buku teks setelah kerajaan menggubal dasar memberi buku teks kepada semua pelajar tanpa mengira dari segi kelayakan. Semua warganegara layak mendapat buku teks.
                                </p>
